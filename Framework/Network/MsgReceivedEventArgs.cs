using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Magic.Network
{
	public class MsgReceivedEventArgs<T> : EventArgs 
	{
		public T message;
		public object sender;
		public MsgReceivedEventArgs(T message, object sender)
		{
			this.message = message;
			this.sender = sender;
		}


	}
}
