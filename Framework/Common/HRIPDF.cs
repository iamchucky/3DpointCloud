using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Magic.Common.Mapack;

namespace Magic.Common
{
	[Serializable]
	public class HRIPDF
	{
		Vector2 mean;
		Matrix2 sigma;
		public HRIPDF(Vector2 mean, Matrix2 sigma)
		{
			this.mean = mean;
			this.sigma = sigma;
		}
		public Vector2 Mean 
		{
			get { return mean; }
		}
		public Matrix2 Sigma
		{
			get { return sigma; }
		}
	}
}
