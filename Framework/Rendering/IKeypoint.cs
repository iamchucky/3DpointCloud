using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Magic.Common;
using Magic.Common.Shapes;
using System.Drawing;

namespace Magic.Rendering.Renderables
{
	public interface IKeypoint : ISelectable
	{
		/// <summary>
		/// color to draw point
		/// </summary>
		Color Color { get; set; }

		/// <summary>
		/// global (?) x-location of point
		/// </summary>
		double X { get; set; }

		/// <summary>
		/// global (?) y-location of point
		/// </summary>
		double Y { get; set; }

		/// <summary>
		/// global (?) z-location of point
		/// </summary>
		double Z { get; set; }
	}
}
